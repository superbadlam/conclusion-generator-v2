package ru.skkdc.lis.utils.expressionevaluators;

public class ExpressionEvaluatorException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public ExpressionEvaluatorException() {
		super();
	}

	public ExpressionEvaluatorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExpressionEvaluatorException(String arg0) {
		super(arg0);
	}

	public ExpressionEvaluatorException(Throwable arg0) {
		super(arg0);
	}

	
	
}
