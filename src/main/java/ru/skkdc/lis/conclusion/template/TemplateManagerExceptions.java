package ru.skkdc.lis.conclusion.template;

public class TemplateManagerExceptions extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public TemplateManagerExceptions() {
		super();
	}

	public TemplateManagerExceptions(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TemplateManagerExceptions(String arg0) {
		super(arg0);
	}

	public TemplateManagerExceptions(Throwable arg0) {
		super(arg0);
	}
	

}
